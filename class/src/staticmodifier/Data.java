package staticmodifier;

public class Data {
    private String nonStaticItem;

    /*
    被 static 修饰符修饰的类的成员变量被称为静态变量.

    类静态变量被类的不同实例 (即类的对象) 共享. 当类的字节码被加载到方法区中时, 虚拟机会在堆内存中一块被称为静态区 (静态存储位置)
    的内存空间中为静态变量开辟存储空间. 而在堆内存中同一个类的不同实例的静态变量指向静态区中的同一块存储空间.

    因此类的静态变量优先于非静态变量被创建, 因为非静态变量的创建需要通过语句:

        new 类的构造函数(参数)

    创建, 而在调用类的构造函数前, 类的字节码需要被加载到方法区中, 这时类的静态变量会立刻在静态区中被创建,
    之后类的对象才会在堆内存中被创建.
    */
    public static String staticItem;


}
