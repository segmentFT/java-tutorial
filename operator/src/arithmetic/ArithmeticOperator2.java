package arithmetic;

public class ArithmeticOperator2 {
    public static void main(String[] args) {
        // division
        System.out.println(10 / 2);
        // 整数之间的运算结果只能是整数
        System.out.println(10 / 3);
        // 浮点数的运算结果可能存在误差
        System.out.println(10 / 3.0);

        // modulus
        System.out.println(10 % 2);
        System.out.println(10 % 3);
    }
}
