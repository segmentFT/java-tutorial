package arithmetic;

public class ArithmeticOperator1 {
    public static void main (String[] args) {
        // plus
        System.out.println(3 + 2);

        // minus
        System.out.println(5 - 1);

        // times
        System.out.println(7 * 9);

        // 浮点数在计算时可能会出现误差
        System.out.println(1.1 + 1.1);
        System.out.println(1.1 + 1.01);
        System.out.println(1.1 - 1.01);
        System.out.println(1.1 * 1.01);
    }
}
